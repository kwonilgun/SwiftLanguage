import Foundation

//Swift is type-safe language. A type safe language encourages you to be clear about the types values your code can work with
//Swift performs type checks when compling your code and flags mismatched types as errors. THis enables you to catch and fix errors as early as possible in the development process

//type inference: If you don't specify the type of value you need, Swift uses type inference to work out the appropriate type.

//Because of type inference, Swift requires far fewer type declarations than any other language.


//type inference example
let meaningOfLife = 42
//meaningOfLife is inferred to be of type Int

let pi = 3.14159
//pi is inferred to be of type Double

let anotherPi = 3 + 0.14159
